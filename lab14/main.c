#include <stdio.h>
#include <setjmp.h>

jmp_buf env;

void fib_sum_rec(int n, int a, int b, int current_sum) {
    if (n == 0) {
        longjmp(env, current_sum);
    }
    fib_sum_rec(n - 1, b, a + b, current_sum + a);
}

int main() {
    int n;
    printf("??????? ?????????? ????? ????????? ??? ????????????: ");
    scanf("%d", &n);

    int result = setjmp(env);
    if (result != 0) {
        printf("????? ?????? %d ????? ?????????: %d\n", n, result);
    } else {
        fib_sum_rec(n, 1, 1, 0);
    }
    return 0;
}

